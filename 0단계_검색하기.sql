-- ============================================
-- 0단계: 전체 목록에서 검색하기
-- ============================================
-- 전체 목록이 너무 많을 때 사용하세요!
-- 아래 검색어 부분만 수정하면 됩니다.

SELECT 
    id AS 광고주ID,
    username AS 광고주명,
    "companyName" AS 회사명
FROM 
    users
WHERE 
    role = 'client'
    AND (
        username ILIKE '%검색어_여기입력%' OR  -- ⬅️ 예: '%국어%' 또는 '%논술%'
        "companyName" ILIKE '%검색어_여기입력%'  -- 회사명으로도 검색
    )
ORDER BY 
    username;

-- ============================================
-- 💡 검색 팁
-- ============================================
-- 1. 일부만 입력해도 됩니다 (예: '국어'만 입력)
-- 2. 광고주명과 회사명 둘 다 검색됩니다
-- 3. 검색어 앞뒤에 % 기호를 반드시 넣으세요

