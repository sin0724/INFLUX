-- ============================================
-- 📌 광고주별 완료건 조회 및 삭제 SQL (초보자용)
-- ============================================
-- 
-- ⚠️ 이 파일은 참고용입니다!
-- 실제 사용은 아래 단계별 파일을 하나씩 실행하세요:
--
-- 1. 0단계_전체광고주목록보기.sql
-- 2. 1단계_광고주검색하기.sql
-- 3. 2단계_완료건목록보기.sql
-- 4. 3단계_완료건삭제하기.sql
--
-- 자세한 사용 방법은 README_사용방법.md 파일을 참고하세요!
--
-- ============================================

-- ============================================
-- 0단계: 전체 광고주 목록 보기 (먼저 실행!)
-- ============================================
-- 파일: 0단계_전체광고주목록보기.sql 을 사용하세요

-- ============================================
-- 1단계: 광고주 검색하기
-- ============================================
-- 파일: 1단계_광고주검색하기.sql 을 사용하세요

-- ============================================
-- 2단계: 그 광고주의 완료건 목록 보기
-- ============================================
-- 파일: 2단계_완료건목록보기.sql 을 사용하세요

-- ============================================
-- 3단계: 선택한 완료건 삭제하기
-- ============================================
-- 파일: 3단계_완료건삭제하기.sql 을 사용하세요

-- ============================================
-- ❗ 자주 발생하는 에러 해결법
-- ============================================

-- 에러 1: "invalid input syntax for type uuid"
-- 원인: 주문ID를 실제 UUID로 바꾸지 않고 '여기에_주문ID_입력' 그대로 실행
-- 해결: 2단계 결과에서 주문ID를 정확히 복사해서 사용하세요

-- 에러 2: 광고주 검색 결과가 없음
-- 해결: 0단계 쿼리로 전체 광고주 목록을 확인하고 정확한 이름을 사용하세요
-- 또는 디버깅 쿼리로 완료건이 있는 광고주만 보세요

-- 에러 3: 완료건 목록이 안 나옴
-- 원인: 해당 광고주에게 완료건이 없거나 검색어가 잘못됨
-- 해결: 광고주 ID로 직접 검색해보세요 (2단계의 두 번째 쿼리 사용)
